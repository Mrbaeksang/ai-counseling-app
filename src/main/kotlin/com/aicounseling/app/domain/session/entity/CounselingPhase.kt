package com.aicounseling.app.domain.session.entity

/**
 * 심리 상담의 진행 단계를 정의하는 Enum 클래스
 * 전통적인 상담 모델과 현대적인 접근을 결합하여 설계됨
 * 학문적 근거에 기반한 체계적인 5단계 상담 과정
 */
enum class CounselingPhase(
    val koreanName: String,
    val description: String,
) {
    /**
     * 1단계: 관계 형성 및 목표 설정
     * 내담자와 신뢰 관계(라포)를 구축하고 상담의 초기 목표를 설정하는 단계
     * 내담자의 참여 동기를 부여하고 상담의 방향성을 정립
     */
    ENGAGEMENT(
        "관계 형성 및 목표 설정",
        "신뢰 관계 구축과 상담 목표 설정으로 참여 동기 부여",
    ),

    /**
     * 2단계: 평가 및 문제 개념화
     * 내담자의 문제를 다각적으로 탐색 및 평가하고,
     * 이를 바탕으로 문제의 핵심 원인과 패턴을 파악하여 사례 개념화를 진행
     */
    ASSESSMENT_AND_CONCEPTUALIZATION(
        "평가 및 문제 개념화",
        "문제의 다각적 탐색과 핵심 원인 및 패턴 파악",
    ),

    /**
     * 3단계: 개입 및 기술 습득
     * 사례 개념화에 기반한 전문적인 상담 기법을 적용하여 변화를 촉진하고,
     * 내담자가 스스로 문제를 다룰 수 있는 새로운 기술과 대처 전략을 습득하도록 지원
     */
    INTERVENTION_AND_SKILL_BUILDING(
        "개입 및 기술 습득",
        "전문 기법 적용과 자립적 문제 해결 기술 습득 지원",
    ),

    /**
     * 4단계: 실행 및 일반화
     * 상담에서 얻은 통찰과 기술을 실제 생활에 적용(실행)하고,
     * 긍정적인 변화를 다양한 상황으로 확장(일반화)하여 내면화
     */
    ACTION_AND_GENERALIZATION(
        "실행 및 일반화",
        "통찰과 기술의 실생활 적용 및 다양한 상황으로 확장",
    ),

    /**
     * 5단계: 종결 및 재발 방지
     * 상담 목표 달성도를 평가하며 과정을 건강하게 마무리하고,
     * 향후 유사한 어려움에 대처할 수 있도록 재발 방지 계획을 수립
     */
    TERMINATION_AND_RELAPSE_PREVENTION(
        "종결 및 재발 방지",
        "목표 달성도 평가와 재발 방지 계획으로 건강한 마무리",
    ),
}
